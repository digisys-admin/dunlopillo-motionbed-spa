  // Îã®Ïùº URL Î°úÎìú (Í∞úÏÑ†Îêú ÏóêÎü¨ Ï≤òÎ¶¨)
  async preloadSingleUrl(url, priority = 'normal', imageName = '') {
    if (this.cache.has(url) || this.failedUrls.has(url)) {
      return Promise.resolve();
    }

    if (this.loadingPromises.has(url)) {
      return this.loadingPromises.get(url);
    }

    const promise = new Promise((resolve, reject) => {
      const img = new Image();
      
      // Ïö∞ÏÑ†ÏàúÏúÑÏóê Îî∞Î•∏ Î°úÎî© Ï†ÑÎûµ
      if (priority === 'critical') {
        img.loading = 'eager';
      } else {
        img.loading = 'lazy';
      }
      
      img.onload = () => {
        this.cache.set(url, img);
        this.preloadQueue.delete(url);
        console.log(`‚úÖ ${imageName} Î°úÎìú ÏôÑÎ£å (${priority}): ${url.includes('thumbnail') ? 'THUMBNAIL' : 'FULLSIZE'}`);
        resolve(img);
      };
      
      img.onerror = () => {
        this.failedUrls.add(url);
        this.preloadQueue.delete(url);
        console.warn(`‚ùå ${imageName} Î°úÎìú Ïã§Ìå®: ${url.includes('thumbnail') ? 'THUMBNAIL' : 'FULLSIZE'}`);
        reject(new Error(`Ïù¥ÎØ∏ÏßÄ Î°úÎìú Ïã§Ìå®: ${url}`));
      };
      
      this.preloadQueue.add(url);
      img.src = url;
    });

    this.loadingPromises.set(url, promise);
    return promise;
  }

